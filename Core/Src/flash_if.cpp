/* Includes ------------------------------------------------------------------*/
#include "flash_if.h"
#include "mem_controller.h"
/* Private typedef -----------------------------------------------------------*/
/* Private define ------------------------------------------------------------*/
/* Private macro -------------------------------------------------------------*/
/* Private variables ---------------------------------------------------------*/
extern Mem_ctrl* memory_extern;
/* Private function prototypes -----------------------------------------------*/

/* Private functions ---------------------------------------------------------*/



/**
  * @brief  This function does an erase of all user flash area
  * @param  start: start of user flash area
  * @retval FLASHIF_OK : user flash area successfully erased
  *         FLASHIF_ERASEKO : error occurred
  */
void FLASH_If_Erase(void)
{
	memory_extern->erase(1);
}	

/* Public functions ---------------------------------------------------------*/
/**
  * @brief  This function writes a data buffer in flash (data are 32-bit aligned).
  * @note   After writing data buffer, the flash content is checked.
  * @param  destination: start address for target location
  * @param  p_source: pointer on buffer with data to write
  * @param  length: length of data buffer (unit is 32-bit word)
  * @retval uint32_t 0: Data successfully written to Flash memory
  *         1: Error occurred while writing data in Flash memory
  *         2: Written Data in flash memory is different from expected one
  */
uint32_t FLASH_If_Write(uint32_t destination, uint8_t *p_source, uint32_t length)
{
	for (uint32_t i = 0; i < length; i++)
	{
		uint32_t value = p_source[i*4]<<24 | p_source[i*4+1]<<16 | p_source[i*4+2]<<8 | p_source[i*4+3];
		memory_extern->write(1, destination + i * 4, value);
	}
	HAL_Delay(30);
  return HAL_OK;
}	



